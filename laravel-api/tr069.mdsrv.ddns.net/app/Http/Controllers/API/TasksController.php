    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {

      $link = env("APP_API_URL").'/tasks/'.$id.'';
      $response = Curl::to($link)
      ->asJson()
      ->delete();
      

      return response()->json(['name' => $response]);
    }
